import 'config.dart';
import 'loading_overlay.dart'; // Import the LoadingOverlay widget

class MyRegister extends StatefulWidget {
  const MyRegister({Key? key}) : super(key: key);

  @override
  _MyRegisterState createState() => _MyRegisterState();
}

class _MyRegisterState extends State<MyRegister> {
  final urlRoot = kIsWeb ? WEB_URL : ANDROID_URL;

  TextEditingController nameController = TextEditingController();
  TextEditingController emailController = TextEditingController();
  TextEditingController passwordController = TextEditingController();
  TextEditingController confirmPasswordController = TextEditingController();
  bool _isNotValite = false;
  bool _isLoading = false;

  void registerUser() async {
    setState(() {
      _isLoading = true;
    });

    if (emailController.text.isNotEmpty &&
        passwordController.text.isNotEmpty &&
        confirmPasswordController.text.isNotEmpty &&
        nameController.text.isNotEmpty) {
      if (confirmPasswordController.text == passwordController.text) {
        var reqBody = {
          'fullName': nameController.text,
          'email': emailController.text,
          'password': passwordController.text
        };

        var res = await http.post(Uri.parse(urlRoot + '/accounts/register'),
            headers: {'Content-Type': 'application/json'},
            body: jsonEncode(reqBody));

        var jsonResponse = jsonDecode(res.body);
        setState(() {
          _isLoading = false;
        });

        if (jsonResponse['code'] == 0) {
          Navigator.pushNamed(context, 'login');
        } else {
          print(jsonResponse['message']);
        }
      } else {
        setState(() {
          _isLoading = false;
        });
        showErrorToast(
            context: context,
            title: 'Error',
            description: 'Password and confirm password is not same!');
      }
    } else {
      setState(() {
        _isNotValite = true;
        _isLoading = false;
      });
    }
  }

